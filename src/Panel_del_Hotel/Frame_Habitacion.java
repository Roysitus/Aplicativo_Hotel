    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        String nro = jTextField3.getText();
        HabitacionSimple s1 = simple.buscar_habitacion(nro);
        HabitacionDoble d1 = doble.buscar_habitacion(nro);
        HabitacionMatrimonial m1 = matrimonial.buscar_habitacion(nro);
        if (s1 == null && d1 == null && m1 == null) {
            JOptionPane.showMessageDialog(null, "El numero de la habitacion que se esta buscando no existe");
        } else {
            if (s1 != null) {
                String estado;
                if (s1.getHabilitado()) {
                    estado = "Desocupado";
                } else {
                    estado = "Ocupado";
                }
                jTextField2.setText(estado);
                jButton6.setEnabled(false);
                jButton7.setEnabled(true);
            }
            if (d1 != null) {
                String estado;
                if (d1.getHabilitado()) {
                    estado = "Desocupado";
                } else {
                    estado = "Ocupado";
                }
                jTextField2.setText(estado);
                jButton6.setEnabled(false);
                jButton7.setEnabled(true);
            }
            if (m1 != null) {
                String estado;
                if (m1.getHabilitado()) {
                    estado = "Desocupado";
                } else {
                    estado = "Ocupado";
                }
                jTextField2.setText(estado);
                jButton6.setEnabled(false);
                jButton7.setEnabled(true);
            }
        }
    }//GEN-LAST:event_jButton6ActionPerformed

