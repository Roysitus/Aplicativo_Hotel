    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nro = jTextField1.getText();
        HabitacionSimple s1 = simple.buscar_habitacion(nro);
        HabitacionDoble d1 = doble.buscar_habitacion(nro);
        HabitacionMatrimonial m1 = matrimonial.buscar_habitacion(nro);
        if (s1 == null && d1 == null && m1 == null) {
            JOptionPane.showMessageDialog(null, "El numero de la habitacion que se esta buscando no existe");
        } else {
            if (s1 != null) {
                jTextArea1.setText(s1.informacion_habitacion());
                JOptionPane.showMessageDialog(null, "Busqueda exitosa Habitacion tipo " + s1.getTipo());
                jButton1.setEnabled(false);
                jButton2.setEnabled(true);
            }
            if (d1 != null) {
                jTextArea1.setText(d1.informacion_habitacion());
                JOptionPane.showMessageDialog(null, "Busqueda exitosa Habitacion tipo " + d1.getTipo());
                jButton1.setEnabled(false);
                jButton2.setEnabled(true);
            }
            if (m1 != null) {
                jTextArea1.setText(m1.informacion_habitacion());
                JOptionPane.showMessageDialog(null, "Busqueda exitosa Habitacion tipo " + m1.getTipo());
                jButton1.setEnabled(false);
                jButton2.setEnabled(true);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

